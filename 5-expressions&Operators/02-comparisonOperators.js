// EQUAL OPERATOR
console.log('3' == 3); //JS assumes and converts to the same type

/*
- coercion is the process of converting a value from one type to another
*/

//STRICT EQUAL OPERATOR
//Strict equal overrides coercion 

console.log('3' === 3); //returns false because coercion is overrode

//NOT EQUAL
console.log('3'!= 3); //false

//STRICT NOT EQUAL
console.log('3' !== 3); //true

//GREATER THAN OPERATOR
console.log(3 > 2);

//LESS THAN OPERATOR
console.log(2 < 3);

//GREATER THAN OR EQUAL TO
console.log(3 >= 2);

//LESS THAN OR EQUAL TO
console.log(2 <= 2);

// AND
2 && 3

// OR
2 || 3